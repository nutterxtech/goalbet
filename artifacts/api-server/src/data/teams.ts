export function getRandomTeamPair(): { home: TeamData; away: TeamData } {
  const shuffled = [...TEAMS].sort(() => Math.random() - 0.5);
  return { home: shuffled[0], away: shuffled[1] };
}

export function getTeamByName(name: string): TeamData | undefined {
  return TEAMS.find(t => t.name === name || t.code === name);
}

export function getTeamsByLeague(league: string): TeamData[] {
  return TEAMS.filter(t => t.league === league);
}
